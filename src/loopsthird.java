import java.util.Scanner;

public class loopsthird {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the value : ");
        int num = sc.nextInt();

        for(int i =1 ; i<11;i++){
            System.out.println(num + " * " + i + " + ");
        }
    }
}
